    private void jButton_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LoginActionPerformed
        
        PreparedStatement ps; //Sql ifadelerini program üzerinden database'e iletip cevap alabilmemizi sağlayan bir nesnedir 
        ResultSet rs; //statement nesnesi kullanılarak SQL sorgusu çalıştıktan sonra veritabanından alınan verileri tutmak için kullanılır.
        ResultSet rs1 = null;
       
        String uname = jTextField1.getText(); 
        String pass = String.valueOf(jPasswordField1.getPassword());
        
        String query ="SELECT * FROM `the_app_user` WHERE `u_uname`=? AND `u_pass`=?";
        
        try {
            ps = MyConnection.getConnection().prepareStatement(query); 
            ps.setString(1, uname);
            ps.setString(2, pass);
            rs = ps.executeQuery();
            int uid = 0;
            if (rs.next()) {
                uid = rs.getInt("u_id");
                if (uid == 1 | uid == 2) {
                    AdminJFrame af = new AdminJFrame();
                    af.setVisible(true);
                    af.pack();
                    af.setLocationRelativeTo(null);
                    this.dispose();
                    af.jLabel2.setText("Welcome "+uname+"");
                } else {
                    JOptionPane.showMessageDialog(null, "Welcome " + uname);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please check your username and password");
                }
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton_LoginActionPerformed
    
    
    
